package inputs;

import java.util.EventListener;

/**
 * Interface qui définit les événements qui peuvent être levés par les objets Input
 * @author Simon Tran
 */
public interface InputListener extends EventListener{
    /**
     * Événement levé lorsque l'entrée est changée
     * @param input l'entrée
     */
    void inputChanged(Object input);

    /**
     * Événement levé lorsque l'entrée n'est pas valide
     * @param input L'entrée
     */
    void invalidInput(Object input);
}